# Pedimos las tres calificaciones
n1 = float(input("Introduce la primera calificación: "))
n2 = float(input("Introduce la segunda calificación: "))
n3 = float(input("Introduce la tercera calificación: "))

# Encontramos la mayor
mayor = max(n1, n2, n3)

# La mostramos
print("La calificación más alta es:", mayor)
